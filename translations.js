// Función para aplicar las traducciones según el idioma seleccionado
function applyTranslations(language) {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-translate");
    const translation = translations[language]?.[key];
    if (translation) {
      el.textContent = translation;
    }
  });

  // Cambiar el título de la página
  document.title = translations[language]?.["page.title"];
}

// Función para establecer el idioma y guardarlo en LocalStorage
function setLanguage(language) {
  // Guardar el idioma en LocalStorage
  localStorage.setItem("selectedLanguage", language);
  // Aplicar las traducciones
  applyTranslations(language);
}

// Establecer idioma predeterminado al cargar la página
document.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem("selectedLanguage") || "es"; // Predeterminado: español
  setLanguage(savedLanguage);

  // Marcar el idioma activo en el menú desplegable
  document.querySelectorAll('.dropdown-item[data-lang]').forEach(item => {
    item.classList.toggle('active', item.getAttribute('data-lang') === savedLanguage);
  });
});

// Escuchar clics en las opciones de idioma
document.querySelectorAll('.dropdown-item[data-lang]').forEach(item => {
  item.addEventListener('click', (e) => {
    e.preventDefault(); // Prevenir el comportamiento predeterminado del enlace
    const selectedLanguage = e.target.getAttribute('data-lang');
    setLanguage(selectedLanguage);

    // Actualizar el estilo activo en el menú
    document.querySelectorAll('.dropdown-item[data-lang]').forEach(el => {
      el.classList.remove('active');
    });
    e.target.classList.add('active');
  });
});
$(document).ready(function () {
  fetch('plusmarcas.json')
    .then(response => response.json())
    .then(data => {
      // Inicializa el DataTable con configuración de idioma
      const table = $('#plusmarcasTable').DataTable({
        language: {
          info: "", // Elimina "Showing X to Y of Z entries"
          paginate: {
            previous: "Anterior",
            next: "Siguiente"
          }
          // Puedes configurar otros textos si lo necesitas, por ejemplo:
          // lengthMenu: "Mostrar _MENU_ registros",
          // zeroRecords: "No se encontraron resultados",
          // infoEmpty: "Mostrando 0 a 0 de 0 registros",
          // infoFiltered: "(filtrado de _MAX_ registros totales)"
        }
      });

      data.forEach(plusmarca => {
        table.row.add([
          plusmarca.nombre,
          plusmarca.categoria,
          plusmarca.modalidad,
          plusmarca.genero,
          plusmarca.marca,
          plusmarca.fecha,
          plusmarca.detalle
        ]).draw();
      });
    })
    .catch(error => console.error('Error al cargar las plusmarcas:', error));
});
